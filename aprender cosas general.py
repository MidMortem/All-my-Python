first = "Salvador"
last = "Bianco"
full = f"{first} {last}"
print(full)
# .upper para mayusculas
print(full.upper())
# .lower para minusculas
print(full.lower())
# .title para poner en mayuscula la primera letra de cada palabra
print(full.title())
# .strip para eliminar espacios al principio y al final
full = "   " + full + "    "
print(full.strip())
# .rstrip para eliminar espacios a la derecha
print(full.rstrip())
# .lstrip para eliminar espacios a la izquierda
print(full.lstrip())
# .capitalize para poner en mayuscula la primera letra de la primera palabra
print(full.capitalize())
# .count para contar cuantas veces aparece una letra o palabra  
print(full.count("a"))
# .find para encontrar la posicion de una letra o palabra (si no la encuentra devuelve -1)
print(full.find("a"))       
# .replace para reemplazar una letra o palabra por otra
print(full.replace("a", "@"))




